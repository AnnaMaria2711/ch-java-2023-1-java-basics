public class relationalOperationen {
    public static void main(String[] args) {

        int a = 10;
        int b = 6;

        int addition = a + b;
        int multiplikation = a * b;
        int subtraktion = a - b;
        int division = a / b;

        System.out.println("10 + 6 = " + addition);
        System.out.println("10 * 6 = " + multiplikation);
        System.out.println("10 / 6 = " + subtraktion);
        System.out.println("10 / 6 = " + division);

        boolean isBigger = a > b;

        boolean isLower = a < b;

        boolean same = a == b;

        System.out.println("Hat a den grösseren Wert als b? " + isBigger);

        System.out.println("Hat a den kleineren Wert als b? " + isLower);

        System.out.println("Hat a den selben Wert wie b? " + same);



        float c = 4.433f;
        double d = 7.4987;


        double addition2 = c + d;
        double multiplikation2 = c * d;
        double subtraktion2 = c - d;
        double division2 = c / d;

        System.out.println("4.433 + 7.4987 = " + addition2);
        System.out.println("4.433 * 7.4987 = " + multiplikation2);
        System.out.println("4.433 / 7.4987 = " + subtraktion2);
        System.out.println("4.433 / 7.4987 = " + division2);

        boolean isBigger2 = a > b;

        boolean isLower2 = a < b;

        boolean same2 = a == b;

        System.out.println("Hat a den grösseren Wert als b? " + isBigger2);

        System.out.println("Hat a den kleineren Wert als b? " + isLower2);

        System.out.println("Hat a den gleichen Wert wie b? " + same2);


    }
}
